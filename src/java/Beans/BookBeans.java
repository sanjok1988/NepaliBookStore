package Beans;


/**
 *
 * @author sanjok
 */

public class BookBeans {

    public final static String table = "books";
    private int id;
    private String name;
    private String price;
    private String author;
    private String publisher;
    private String publised_date;
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the price
     */
    public String getPrice() {
        return price;
    }

    /**
     * @param price the price to set
     */
    public void setPrice(String price) {
        this.price = price;
    }

    /**
     * @return the author
     */
    public String getAuthor() {
        return author;
    }

    /**
     * @param author the author to set
     */
    public void setAuthor(String author) {
        this.author = author;
    }

    /**
     * @return the publisher
     */
    public String getPublisher() {
        return publisher;
    }

    /**
     * @param publisher the publisher to set
     */
    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    /**
     * @return the publised_date
     */
    public String getPublised_date() {
        return publised_date;
    }

    /**
     * @param publised_date the publised_date to set
     */
    public void setPublised_date(String publised_date) {
        this.publised_date = publised_date;
    }

    
    
}
